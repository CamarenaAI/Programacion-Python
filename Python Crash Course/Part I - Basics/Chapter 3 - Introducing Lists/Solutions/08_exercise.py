places = ['Paris', 'London', 'Rome', 'New York', 'Tokio']

print(places)

# Alphabetic order
print(sorted(places))

# Reverse alphabetic order
print(sorted(places, reverse=True))

# Change order of your list
places.reverse()
print(places)

# Change order of your list again
places.reverse()
print(places)

# Alphabetic order
places.sort()
print(places)

# Reverse alphabetic order
places.sort(reverse=True)
print(places)