# Enteros
# Puedes sumar (+), restar (-), multiplicar (*), y dividir (/) numeros enteros en Python
print(2 + 3)
print(3 - 2)
print(2 * 3)
print(3 / 2)

# Python usa el simbolo de multiplicacion dos veces para representar exponentes(**)
print(3 ** 2)
print(3 ** 3)
print(10 ** 6)

# Python tambien soporta el orden de operaciones, puedes usar multiples operaciones en una expresion
print(2 + 3 * 4)
print((2 + 3) * 4)

