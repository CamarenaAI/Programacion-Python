# 3.8 Seeing the World
places = ['Paris', 'London', 'Rome', 'New York', 'Tokio']

print(places)
print(sorted(places)) # Alphabetic order
print(sorted(places, reverse=True)) # Reverse alphabetic order
places.reverse() # Change order of your list
print(places)
places.reverse() # Change order of your list again
print(places)
places.sort() # Alphabetic order
print(places)
places.sort(reverse=True) # Reverse alphabetic order
print(places)